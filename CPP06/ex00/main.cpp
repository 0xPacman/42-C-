#include "Conv.h"

int main(int argc, char **argv)
{
    if (argc != 2)
    {
        std::cout << "Usage: ./convert [string]" << std::endl;
        return (1);
    }
    try
    {
        conv c(argv[1]);
        c.convert();
    }
    catch (std::exception &e)
    {
        std::cout << e.what() << std::endl;
    }
    return (0);
}